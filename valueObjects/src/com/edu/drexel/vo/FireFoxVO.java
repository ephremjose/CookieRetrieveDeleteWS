package com.edu.drexel.vo;

/*
 * FireFoxVO is the bean class with setters and getters for variables.
 * 
 */

public class FireFoxVO {
	
	private String id;
	private String baseDomain;
	private String name;
	private String value;
	private String host;
	private String creationTime;
	private String lastAccessed;
	private String expiry;
	
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the baseDomain
	 */
	public String getBaseDomain() {
		return baseDomain;
	}
	/**
	 * @param baseDomain the baseDomain to set
	 */
	public void setBaseDomain(String baseDomain) {
		this.baseDomain = baseDomain;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the value
	 */
	public String getValue() {
		return value;
	}
	/**
	 * @param value the value to set
	 */
	public void setValue(String value) {
		this.value = value;
	}
	/**
	 * @return the host
	 */
	public String getHost() {
		return host;
	}
	/**
	 * @param host the host to set
	 */
	public void setHost(String host) {
		this.host = host;
	}
	/**
	 * @return the creationTime
	 */
	public String getCreationTime() {
		return creationTime;
	}
	/**
	 * @param creationTime the creationTime to set
	 */
	public void setCreationTime(String creationTime) {
		this.creationTime = creationTime;
	}
	/**
	 * @return the lastAccessed
	 */
	public String getLastAccessed() {
		return lastAccessed;
	}
	/**
	 * @param lastAccessed the lastAccessed to set
	 */
	public void setLastAccessed(String lastAccessed) {
		this.lastAccessed = lastAccessed;
	}
	/**
	 * @return the expiry
	 */
	public String getExpiry() {
		return expiry;
	}
	/**
	 * @param expiry the expiry to set
	 */
	public void setExpiry(String expiry) {
		this.expiry = expiry;
	}
	
	
}
